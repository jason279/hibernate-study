package jason.self.fms.service.util;

public class InconsistentVersionException extends Exception {
	public InconsistentVersionException() {
	}

	public InconsistentVersionException(String arg0) {
		super(arg0);
	}

	public InconsistentVersionException(Throwable arg0) {
		super(arg0);
	}

	public InconsistentVersionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
